package entities;

public class Origin {
    private int id;
    private String name;

    public Origin() {
    }

    public Origin(int id, String name) {
        this.id = id;
        this.name = name;
    }
    
    public Origin(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
}